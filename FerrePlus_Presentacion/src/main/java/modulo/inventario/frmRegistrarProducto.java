    private void registro() throws NegocioException {
        if (ControlGUI.getInstancia().ValidarRegistroProducto(this.txtSKU.getText(),
                this.txtNombre.getText(),
                this.txtPrecioVenta.getText(),
                this.txtPrecioCompra.getText(),
                this.txtStock.getText()) == true) {
            ControlGUI.getInstancia().registrarProducto(ControlGUI.getInstancia().crearProductoDTO(this.txtSKU.getText(),
                    this.txtNombre.getText(),
                    this.jComboBoxCategoria.getItemAt(this.jComboBoxCategoria.getSelectedIndex()),
                    this.jComboBoxUnidad.getItemAt(this.jComboBoxUnidad.getSelectedIndex()),
                    this.txtPrecioCompra.getText(),
                    this.txtPrecioVenta.getText(),
                    this.txtProveedor.getText(),
                    this.txtStock.getText(),
                    this.txtObservaciones.getText()));
        }

    }


    public void combobox() {
        this.jComboBoxCategoria.addItem("General");
        this.jComboBoxUnidad.addItem("KG");
        this.jComboBoxUnidad.addItem("Litros");
        this.jComboBoxUnidad.addItem("Gramos");
    }

    /**
     * Metodo para el ajuste de los componentes cuando la pantalla cambie de
     * tama√±o.
     */

    private void AcomodarContenido() {
        JPanel panel = this.pnlFormulario;
        this.pnlFormulario.setLayout(new GridBagLayout());

        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridy = 0; // Todos estar√°n en la misma fila (fila 0), se incrementa para mover a la siguiente fila.
        gbc.weightx = 1.0; // Cada componente se expandir√° horizontalmente de manera uniforme.
        gbc.fill = GridBagConstraints.HORIZONTAL; // Hace que el componente llene el espacio horizontalmente.
        gbc.insets = new Insets(5, 5, 5, 5); // Margen alrededor del componente.

        // Columna 0
        gbc.gridx = 0;
        panel.add(this.lblSKU, gbc);
        gbc.gridy++;
        panel.add(this.lblNombre, gbc);
        gbc.gridy++;
        panel.add(this.lblPrecio, gbc);
        gbc.gridy++;
        panel.add(this.lblPrecio2, gbc);
        gbc.gridy++;
        panel.add(this.lblProveedor, gbc);
        gbc.gridy++;

        // Columna 1
        gbc.gridx = 1;
        gbc.gridy = 0;
        panel.add(this.txtSKU, gbc);
        gbc.gridy++;
        panel.add(this.txtNombre, gbc);
        gbc.gridy++;
        panel.add(this.txtPrecioVenta, gbc);
        gbc.gridy++;
        panel.add(this.txtPrecioCompra, gbc);
        gbc.gridy++;
        panel.add(this.txtProveedor, gbc);
        gbc.gridy++;
        panel.add(this.jButtonCancelar, gbc);
        gbc.gridy++;
        panel.add(this.jButtonRegistrar, gbc);

        // Columna 2
        gbc.gridx = 2;
        gbc.gridy = 0;
        panel.add(this.lblObservaciones, gbc);
        gbc.gridy++;
        panel.add(this.lblUnidad, gbc);
        gbc.gridy++;
        panel.add(this.lblCategoria, gbc);
        gbc.gridy++;
        panel.add(this.lblStock, gbc);
        gbc.gridy++;

        // Columna 3
        gbc.gridx = 3;
        gbc.gridy = 0;
        panel.add(this.txtObservaciones, gbc);
        gbc.gridy++;
        panel.add(this.jComboBoxUnidad, gbc);
        gbc.gridy++;
        panel.add(this.jComboBoxCategoria, gbc);
        gbc.gridy++;
        panel.add(this.txtStock, gbc);
        gbc.gridy++;
    }

